#------------------------------------------------------------------------
# 참조 목록.
#------------------------------------------------------------------------
import os


#------------------------------------------------------------------------
# 알타바.
#------------------------------------------------------------------------
class Altava:
	#------------------------------------------------------------------------
	# 스태틱 멤버 변수 목록.
	#------------------------------------------------------------------------
	IsBuild : bool
	IsDebug : bool
	ProjectPath : str

	#------------------------------------------------------------------------
	# 참조 목록.
	#------------------------------------------------------------------------
	@staticmethod
	def IsBuildMode() -> bool:
		return Altava.IsBuild
	
	#------------------------------------------------------------------------
	# 디버깅 여부.
	#------------------------------------------------------------------------
	@staticmethod
	def IsDebugMode() -> bool:
		return Altava.IsDebug
	
	#------------------------------------------------------------------------
	# 실행 경로.
	#------------------------------------------------------------------------
	@staticmethod
	def GetProjectPath() -> str:
		return Altava.ProjectPath